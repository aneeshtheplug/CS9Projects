from lab04 import solveMaze

def test__solveMaze():
    maze = [
['+','+','+','+','G','+'],
['+',' ','+',' ',' ','+'],
['+',' ',' ',' ','+','+'],
['+',' ','+','+',' ','+'],
['+',' ',' ',' ',' ','+'],
['+','+','+','+','+','+'] ]
    assert solveMaze(maze, 4, 4) == True
    assert maze == [
['+','+', '+', '+', 'G', '+'],
['+', 8, '+', 11, 12, '+'],
['+', 7, 9, 10, '+', '+'],
['+', 6, '+', '+', 2, '+'],
['+', 5, 4, 3, 1, '+'],
['+', '+', '+', '+', '+', '+'] ]
    maze1 = [
['+','+','+','+','G','+'],
['+',' ','+',' ',' ','+'],
['+',' ',' ',' ','+','+'],
['+',' ','+','+',' ','+'],
['+','+',' ',' ',' ','+'],
['+','+','+','+','+','+'] ]
    assert solveMaze(maze1, 4, 4) == False
    assert maze1 == [
['+', '+', '+', '+', 'G', '+'], 
['+', ' ', '+', ' ', ' ', '+'], 
['+', ' ', ' ', ' ', '+', '+'], 
['+', ' ', '+', '+', 2, '+'], 
['+', '+', 4, 3, 1, '+'], 
['+', '+', '+', '+', '+', '+'] ]
    maze2 = [
['+','+','+','+','G','+'],
['+',' ','+',' ',' ','+'],
['+',' ',' ',' ','+','+'],
['+','+','+','+','+','+']]
    assert solveMaze(maze2, 2, 1) == True
    assert maze2 == [
['+','+','+','+','G','+'],
['+', 2,'+', 5, 6,'+'],
['+', 1, 3, 4,'+','+'],
['+','+','+','+','+','+']]